<script>
    //Модалка авторизации
    var modalLogin = document.getElementById("loginModal");
    var btnLogin = document.getElementById("login");
    var btnLoginModal = document.getElementById("spanLoginModal");
    var spanLogin = document.getElementById("closeLogin");

    var modalReg = document.getElementById("regModal");
    var btnReg = document.getElementById("register");
    var spanReg = document.getElementById("closeReg");

    btnLogin.onclick = function () {
        modalLogin.style.display = "block";
    }

    btnLoginModal.onclick = function () {
        modalReg.style.display = "none";
        modalLogin.style.display = "block";
    }

    spanLogin.onclick = function () {
        modalLogin.style.display = "none";
    }

    btnReg.onclick = function () {
        modalLogin.style.display = "none";
        modalReg.style.display = "block";
    }

    spanReg.onclick = function () {
        modalReg.style.display = "none";
    }

    var formLogin = document.getElementById("loginForm");

    formLogin.onsubmit = async (e) => {
        e.preventDefault();

        let response = await fetch('/login/auth', {
            method: 'POST',
            body: new FormData(formLogin)
        });

        let result = await response.json();
    };

    var formReg = document.getElementById("formReg");

    formReg.onsubmit = async (e) => {
        e.preventDefault();

        let response = await fetch('/login/register', {
            method: 'POST',
            body: new FormData(formReg)
        });

        let result = await response.json();
    };
</script>
